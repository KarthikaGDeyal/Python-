x="Python"
print("*** String Comparison Functions In Python ***")
print("Our String is:",x)
print("Using isupper() method:",x.isupper())
print("Using islower() method:",x.islower())
print("Using isalpha() method:",x.isalpha())
print("Using isalnum() method:",x.isalnum())
print("Using isnumeric() method:",x.isnumeric())